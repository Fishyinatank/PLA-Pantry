create or replace function public.is_developer()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.developer_access da
    where lower(da.email) = lower(coalesce(auth.jwt() ->> 'email', ''))
      and da.revoked_at is null
      and (da.expires_at is null or da.expires_at > now())
  );
$$;

