create table if not exists public.developer_access (
  id bigint generated by default as identity primary key,
  email text unique not null,
  created_at timestamptz not null default now(),
  created_by uuid references auth.users(id) on delete set null default auth.uid(),
  expires_at timestamptz,
  revoked_at timestamptz,
  is_protected boolean not null default false
);

create table if not exists public.filament_brands (
  id bigint generated by default as identity primary key,
  name text unique not null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.material_families (
  id bigint generated by default as identity primary key,
  name text unique not null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.material_subtypes (
  id bigint generated by default as identity primary key,
  family_id bigint not null references public.material_families(id) on delete cascade,
  name text not null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique(family_id, name)
);

