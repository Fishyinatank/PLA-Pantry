create or replace function public.normalize_developer_access()
returns trigger
language plpgsql
as $$
begin
  new.email = lower(trim(new.email));

  if new.email = 'vishrutsn@gmail.com' then
    new.is_protected = true;
    new.expires_at = null;
    new.revoked_at = null;
  end if;

  if tg_op = 'UPDATE' and old.email = 'vishrutsn@gmail.com' then
    if new.email <> old.email or new.is_protected is not true or new.expires_at is not null or new.revoked_at is not null then
      raise exception 'The primary developer cannot be changed, expired, or revoked.';
    end if;
  end if;

  return new;
end;
$$;

create or replace function public.prevent_primary_developer_delete()
returns trigger
language plpgsql
as $$
begin
  if old.email = 'vishrutsn@gmail.com' then
    raise exception 'The primary developer cannot be deleted.';
  end if;
  return old;
end;
$$;

