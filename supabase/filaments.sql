create table if not exists public.print_logs (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  print_name text not null,
  description text,
  filament_id bigint references public.filaments(id) on delete set null,
  filament_label text,
  grams_used numeric(10, 2) not null,
  filament_before_grams numeric(10, 2),
  filament_after_grams numeric(10, 2),
  mode text not null default 'one_time' check (mode in ('one_time', 'new', 'reprint')),
  printed_at timestamptz not null default now()
);

alter table public.print_logs add column if not exists filament_before_grams numeric(10, 2);
alter table public.print_logs add column if not exists filament_after_grams numeric(10, 2);

